public class CalculadorDiferencaIdade {
    public static void main(String[] args) {
        int primeiraIdade = 20;
        int segundaIdade = 30;
        int diferencaIdade = segundaIdade - primeiraIdade;

        System.out.println("A primeira idade é: " + primeiraIdade + " anos");
        System.out.println("A segunda idade é: " + segundaIdade + " anos");
        System.out.println("A diferença entre as duas é de " + diferencaIdade + " anos");
    }
}
